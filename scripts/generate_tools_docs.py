@dataclass
class ToolInfo:
    """Snapshot of a tool's relevant ClassVars and source paths."""

    cls: type
    name: str
    version: str
    summary: str
    category: str
    permissions: list[str]
    requires_approval: bool
    requires_config: bool
    core_tool: bool
    available: bool
    rate_limit_per_minute: int
    timeout_seconds: int
    use_circuit_breaker: bool
    always_background: bool
    background_threshold_seconds: Optional[int]
    supports_multiple_configs: bool
    config_namespace: Optional[str]
    config_schema: Optional[dict]
    config_defaults: dict
    state_schema: Optional[dict]
    state_defaults: dict
    reset_policy: str
    params_schema: Optional[dict]
    module_doc: Optional[str]
    class_doc: Optional[str]
    source_file: Path
    source_rel: str  # relative to repo root, e.g. src/mcp_server/tools/...

    @classmethod
    def from_class(cls, tool_cls: type) -> "ToolInfo":
        try:
            src = inspect.getsourcefile(tool_cls) or ""
        except TypeError:
            src = ""
        source_file = Path(src) if src else Path()
        try:
            source_rel = str(source_file.resolve().relative_to(ROOT))
        except (ValueError, OSError):
            source_rel = src

        module = inspect.getmodule(tool_cls)
        module_doc = inspect.getdoc(module) if module else None
        class_doc = inspect.getdoc(tool_cls)

        # ClassVars: read with ``getattr`` so we honour inherited defaults.
        return cls(
            cls=tool_cls,
            name=getattr(tool_cls, "name", tool_cls.__name__),
            version=getattr(tool_cls, "version", "1.0.0"),
            summary=getattr(tool_cls, "summary", "") or "",
            category=getattr(tool_cls, "category", "general"),
            permissions=list(getattr(tool_cls, "permissions", []) or []),
            requires_approval=bool(getattr(tool_cls, "requires_approval", False)),
            requires_config=bool(getattr(tool_cls, "requires_config", False)),
            core_tool=bool(getattr(tool_cls, "core_tool", False)),
            available=bool(getattr(tool_cls, "available", True)),
            rate_limit_per_minute=int(getattr(tool_cls, "rate_limit_per_minute", 60)),
            timeout_seconds=int(getattr(tool_cls, "timeout_seconds", 30)),
            use_circuit_breaker=bool(getattr(tool_cls, "use_circuit_breaker", True)),
            always_background=bool(getattr(tool_cls, "always_background", False)),
            background_threshold_seconds=getattr(
                tool_cls, "background_threshold_seconds", None
            ),
            supports_multiple_configs=bool(
                getattr(tool_cls, "supports_multiple_configs", False)
            ),
            config_namespace=getattr(tool_cls, "config_namespace", None),
            config_schema=getattr(tool_cls, "config_schema", None),
            config_defaults=dict(getattr(tool_cls, "config_defaults", {}) or {}),
            state_schema=getattr(tool_cls, "state_schema", None),
            state_defaults=dict(getattr(tool_cls, "state_defaults", {}) or {}),
            reset_policy=getattr(tool_cls, "reset_policy", "never"),
            params_schema=getattr(tool_cls, "params_schema", None),
            module_doc=module_doc,
            class_doc=class_doc,
            source_file=source_file,
            source_rel=source_rel,
        )

    @property
    def has_config(self) -> bool:
        return bool(self.config_schema) or bool(self.config_defaults)


@dataclass
class ToolGroup:
    """A namespace group (≥1 tool) or a singleton tool."""

    group_id: str  # namespace if shared, else tool name
    namespace: Optional[str]  # None for singletons without namespace
    tools: list[ToolInfo] = field(default_factory=list)

    @property
    def is_family(self) -> bool:
        return len(self.tools) > 1

    @property
    def display_title(self) -> str:
        if self.namespace and self.is_family:
            pretty = self.namespace.replace("_", " ").title()
            return f"{pretty} Tools"
        if len(self.tools) == 1:
            t = self.tools[0]
            pretty = t.name.replace("_", " ").title()
            return pretty
        return self.group_id


