def render_group_doc(group: ToolGroup, depth: str) -> str:
    parts: list[str] = []
    parts.append(AUTOGEN_BANNER)
    parts.append(f"# {group.display_title}\n")

    overview = render_overview(group)
    parts.append("## Overview\n\n" + overview + "\n")

    if group.is_family:
        parts.append("## Tools in this group\n\n" + render_tools_table(group) + "\n")
    else:
        parts.append("## Tool\n\n" + render_tools_table(group) + "\n")

    # Configuration block — shared across the family or per the singleton.
    has_any_config = any(t.has_config for t in group.tools)
    if has_any_config:
        parts.append("## Configuration\n")

        notes: list[str] = []
        notes.append(
            "The JSON below goes into the **configtool model** in the web UI "
            "(per organisation; can also be scoped to a department)."
        )
        if any(t.supports_multiple_configs for t in group.tools):
            notes.append(
                "**Multiple profiles supported** — you may create more than one "
                "config row per organisation, each with a distinct `profile_id`. "
                "The agent picks one via the `config_profile` parameter at call time. "
                "State, rate limits and circuit-breaker counters are isolated per profile."
            )
        if any(
            (p.get("sensitive") for p in schema_properties(t.config_schema).values())
            for t in group.tools
        ):
            notes.append(
                "**Sensitive fields** (api keys / passwords / tokens) are *omitted* "
                "from the JSON below — set them via environment variables (see the "
                "_Environment variables_ table) or directly in the encrypted DB row "
                "from the web UI."
            )
        for note in notes:
            parts.append(f"> {note}\n")

        if group.is_family and group.namespace:
            ns_props, ns_required, ns_defaults = merge_schemas(group.tools)
        else:
            t = group.tools[0]
            ns_props = schema_properties(t.config_schema)
            ns_required = required_fields(t.config_schema)
            ns_defaults = dict(t.config_defaults)

        skeleton = render_config_skeleton(ns_props, ns_required, ns_defaults)
        example = render_config_example(ns_props, ns_defaults)
        parts.append("### Empty configtool template (required fields only)\n")
        parts.append("```json\n" + skeleton + "\n```\n")
        parts.append("### Example configtool model\n")
        parts.append("```json\n" + example + "\n```\n")
        if ns_props:
            parts.append("### Field reference\n")
            parts.append(render_field_reference(ns_props, ns_required, ns_defaults) + "\n")

    # Environment variables.
    parts.append("## Environment variables\n")
    parts.append(
        "The variables below are derived automatically from each tool's "
        "`config_schema`. Set them in your `.env` (or via your secrets backend) "
        "to seed defaults for every organisation. **Per-org DB config takes "
        "precedence over env vars.**\n"
    )
    parts.append(render_env_table(group))
    loose = render_loose_env_table(group)
    if loose:
        parts.append(loose)
    parts.append("")

    # Operations / actions.
    op_blocks: list[str] = []
    for t in group.tools:
        block = render_actions_section(t, depth)
        if block:
            op_blocks.append(f"### `{t.name}`\n\n{block}")
    if op_blocks:
        parts.append("## Operations\n")
        parts.append("\n\n".join(op_blocks) + "\n")

    # Permissions + Operational metadata.
    parts.append("## Permissions required\n\n" + render_permissions(group) + "\n")
    parts.append("## Operational metadata\n\n" + render_metadata(group) + "\n")
    parts.append("## Source files\n\n" + render_source_files(group) + "\n")

    return "\n".join(parts).rstrip() + "\n"


