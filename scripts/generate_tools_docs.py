def render_overview(group: ToolGroup) -> str:
    """Pick a "primary" tool's docstring as the group overview."""
    primary = next(
        (t for t in group.tools if t.name == group.group_id),
        group.tools[0],
    )
    text = primary.module_doc or primary.class_doc or ""
    para = first_paragraph(text)
    return para or "_No description provided._"


def render_tools_table(group: ToolGroup) -> str:
    rows: list[str] = [
        "| Tool | Summary | Permissions | Requires approval | Core |",
        "| --- | --- | --- | :---: | :---: |",
    ]
    for t in group.tools:
        perms = ", ".join(f"`{p}`" for p in t.permissions) or "—"
        rows.append(
            "| `{name}` | {summary} | {perms} | {appr} | {core} |".format(
                name=md_escape_cell(t.name),
                summary=md_escape_cell(t.summary or first_paragraph(t.class_doc) or "—"),
                perms=md_escape_cell(perms),
                appr=md_bool(t.requires_approval),
                core=md_bool(t.core_tool),
            )
        )
    return "\n".join(rows)


def render_config_skeleton(
    props: dict[str, dict], required: list[str], defaults: dict
) -> str:
    """Minimal JSON skeleton for the configtool.  Sensitive fields are omitted."""
    skeleton: dict[str, Any] = {}
    for fname in required:
        finfo = props.get(fname, {})
        if finfo.get("sensitive"):
            continue
        default = field_default(props, defaults, fname)
        skeleton[fname] = default if default is not None else _placeholder_for(finfo)
    if not skeleton:
        return "{}"
    return json.dumps(skeleton, indent=2, ensure_ascii=False)


def render_config_example(props: dict[str, dict], defaults: dict) -> str:
    """Example JSON with placeholders for every (non-sensitive) field."""
    payload: dict[str, Any] = {}
    for fname in sorted(props.keys()):
        finfo = props[fname]
        if finfo.get("sensitive"):
            # Sensitive fields go through env vars, not the JSON.
            continue
        default = field_default(props, defaults, fname)
        payload[fname] = default if default is not None else _placeholder_for(finfo)
    if not payload:
        return "{}"
    return json.dumps(payload, indent=2, ensure_ascii=False)


def _placeholder_for(finfo: dict) -> Any:
    t = finfo.get("type")
    if t == "boolean":
        return False
    if t == "integer":
        return 0
    if t == "number":
        return 0.0
    if t == "array":
        return []
    if t == "object":
        return {}
    enum_vals = finfo.get("enum")
    if enum_vals:
        return enum_vals[0]
    return "<value>"


def render_field_reference(
    props: dict[str, dict], required: list[str], defaults: dict
) -> str:
    rows = [
        "| Field | Type | Required | Sensitive | Default | Description |",
        "| --- | --- | :---: | :---: | --- | --- |",
    ]
    for fname in sorted(props.keys()):
        finfo = props[fname]
        default = field_default(props, defaults, fname)
        default_repr = (
            "—" if default is None else f"`{json.dumps(default, ensure_ascii=False)}`"
        )
        if finfo.get("sensitive"):
            default_repr = "—"
        rows.append(
            "| `{f}` | `{t}` | {req} | {sens} | {default} | {desc} |".format(
                f=md_escape_cell(fname),
                t=md_escape_cell(schema_type(finfo)),
                req=md_bool(fname in required),
                sens=md_bool(bool(finfo.get("sensitive"))),
                default=md_escape_cell(default_repr),
                desc=md_escape_cell(finfo.get("description") or "—"),
            )
        )
    return "\n".join(rows)


def render_env_table(group: ToolGroup) -> str:
    """Render the canonical config-derived env-var table for a group.

    Layout:
      - Rows under ``TOOL_{NAMESPACE}__*`` when the group has a namespace.
      - Rows under ``TOOL_{TOOL_NAME}__*`` for tool-specific overrides
        (only fields the tool exposes that are not part of the shared
        namespace surface — useful for legacy singletons too).
    """
    rows = [
        "| Variable | Maps to field | Type | Default | Sensitive | Description |",
        "| --- | --- | --- | --- | :---: | --- |",
    ]
    seen_vars: set[str] = set()
    namespace_fields: set[str] = set()

    if group.namespace and group.is_family:
        ns_props, ns_required, ns_defaults = merge_schemas(group.tools)
        namespace_fields = set(ns_props.keys())
        for fname in sorted(ns_props.keys()):
            finfo = ns_props[fname]
            var = env_var_name(group.namespace, fname)
            seen_vars.add(var)
            default = field_default(ns_props, ns_defaults, fname)
            default_repr = (
                "—"
                if default is None or finfo.get("sensitive")
                else f"`{json.dumps(default, ensure_ascii=False)}`"
            )
            rows.append(
                "| `{v}` | `{f}` | `{t}` | {d} | {s} | {desc} |".format(
                    v=var,
                    f=md_escape_cell(fname),
                    t=md_escape_cell(schema_type(finfo)),
                    d=md_escape_cell(default_repr),
                    s=md_bool(bool(finfo.get("sensitive"))),
                    desc=md_escape_cell(finfo.get("description") or "—"),
                )
            )

    for t in group.tools:
        scope = t.name
        if t.config_namespace and t.config_namespace == t.name:
            continue
        props = schema_properties(t.config_schema)
        if not props and not t.config_defaults:
            continue
        for fname in sorted(props.keys() | t.config_defaults.keys()):
            # When the field is already represented by the shared namespace
            # variable, do NOT also emit the per-tool override row — every
            # field accepts a per-tool override implicitly.
            if fname in namespace_fields:
                continue
            finfo = props.get(fname, {})
            var = env_var_name(scope, fname)
            if var in seen_vars:
                continue
            seen_vars.add(var)
            default = field_default(props, t.config_defaults, fname)
            default_repr = (
                "—"
                if default is None or finfo.get("sensitive")
                else f"`{json.dumps(default, ensure_ascii=False)}`"
            )
            rows.append(
                "| `{v}` | `{f}` | `{t}` | {d} | {s} | {desc} |".format(
                    v=var,
                    f=md_escape_cell(fname),
                    t=md_escape_cell(schema_type(finfo)),
                    d=md_escape_cell(default_repr),
                    s=md_bool(bool(finfo.get("sensitive"))),
                    desc=md_escape_cell(finfo.get("description") or "—"),
                )
            )
    if len(rows) == 2:
        return "_No configuration-derived environment variables._"
    note = (
        "\n\n_Note: any field above can also be overridden per-tool by "
        "using the prefix `TOOL_<TOOL_NAME>__` instead of the shared "
        "`TOOL_<NAMESPACE>__` — useful when a single tool in the family "
        "needs a distinct value._"
        if group.namespace and group.is_family
        else ""
    )
    return "\n".join(rows) + note


def render_loose_env_table(group: ToolGroup) -> str:
    sources = [t.source_file for t in group.tools if t.source_file.exists()]
    pairs = scan_loose_env_vars(sources)
    if not pairs:
        return ""
    rows = [
        "",
        "**Other environment variables** (read directly from the source — not part of `config_schema`):",
        "",
        "| Variable | Default in code |",
        "| --- | --- |",
    ]
    for name, default in pairs:
        d_repr = (
            "—"
            if default is None
            else f"`{json.dumps(default, ensure_ascii=False)}`"
        )
        rows.append(f"| `{name}` | {d_repr} |")
    return "\n".join(rows)


def render_actions_section(t: ToolInfo, depth: str) -> str:
    """Render the action / operations block for a tool.

    A tool is considered "action-driven" when its ``params_schema`` has a
    top-level ``properties.action.enum`` list.
    """
    schema = t.params_schema or {}
    props = schema.get("properties") if isinstance(schema, dict) else None
    if not isinstance(props, dict):
        return ""
    action_def = props.get("action")
    if not isinstance(action_def, dict):
        return ""
    enum = action_def.get("enum")
    if not isinstance(enum, list) or not enum:
        return ""

    raw_desc = action_def.get("description") or ""
    per_action = _split_action_descriptions(raw_desc, enum)

    lines: list[str] = []
    for value in enum:
        lines.append(f"- **`{value}`** — {per_action.get(value, '_(no description)_')}")
        if depth == "medium":
            relevant = _params_for_action(props, value, raw_desc)
            if relevant:
                lines.append("")
                lines.append(
                    "  | Parameter | Type | Required | Description |"
                )
                lines.append("  | --- | --- | :---: | --- |")
                req_top = required_fields(schema)
                for pname, pinfo in relevant:
                    lines.append(
                        "  | `{n}` | `{t}` | {r} | {d} |".format(
                            n=md_escape_cell(pname),
                            t=md_escape_cell(schema_type(pinfo)),
                            r=md_bool(pname in req_top),
                            d=md_escape_cell(pinfo.get("description") or "—"),
                        )
                    )
                lines.append("")
    return "\n".join(lines)


_ACTION_LINE_RE = re.compile(r"^\s*-\s*([A-Za-z0-9_]+)\s*[:\u2014-]\s*(.+)$")


def _split_action_descriptions(text: str, enum_values: list[str]) -> dict[str, str]:
    """Best-effort split of the action enum description into per-action entries.

    Handles the common pattern used in the codebase::

        - add_items: add one or more entries to a collection ...
        - del_item: remove an entry ...
    """
    out: dict[str, str] = {}
    for line in text.splitlines():
        m = _ACTION_LINE_RE.match(line)
        if m:
            key = m.group(1).strip()
            desc = m.group(2).strip()
            if key in enum_values:
                out[key] = desc
    return out


def _params_for_action(
    props: dict, action_value: str, action_desc: str
) -> list[tuple[str, dict]]:
    """Heuristic: list params likely relevant to a given action.

    We look for property descriptions that mention the action name; if no
    matches are found we fall back to ALL non-action top-level parameters
    (better to over-show than under-show in `medium` depth).
    """
    relevant: list[tuple[str, dict]] = []
    for name, info in props.items():
        if name == "action":
            continue
        if not isinstance(info, dict):
            continue
        desc = (info.get("description") or "").lower()
        if action_value.lower() in desc:
            relevant.append((name, info))
    if not relevant:
        relevant = [
            (n, i) for n, i in props.items() if n != "action" and isinstance(i, dict)
        ]
    return relevant


def render_permissions(group: ToolGroup) -> str:
    rows = ["| Tool | Permissions |", "| --- | --- |"]
    for t in group.tools:
        perms = ", ".join(f"`{p}`" for p in t.permissions) or "—"
        rows.append(f"| `{t.name}` | {perms} |")
    return "\n".join(rows)


def render_metadata(group: ToolGroup) -> str:
    rows = [
        "| Tool | Version | Rate limit/min | Timeout (s) | Circuit breaker | Background | Multi-config |",
        "| --- | --- | ---: | ---: | :---: | :---: | :---: |",
    ]
    for t in group.tools:
        bg = "always" if t.always_background else (
            f">{t.background_threshold_seconds}s" if t.background_threshold_seconds else "—"
        )
        rows.append(
            "| `{n}` | `{v}` | {rl} | {to} | {cb} | {bg} | {mc} |".format(
                n=t.name,
                v=t.version,
                rl=t.rate_limit_per_minute,
                to=t.timeout_seconds,
                cb=md_bool(t.use_circuit_breaker),
                bg=bg,
                mc=md_bool(t.supports_multiple_configs),
            )
        )
    return "\n".join(rows)


def render_source_files(group: ToolGroup) -> str:
    seen: set[str] = set()
    items: list[str] = []
    for t in group.tools:
        if t.source_rel and t.source_rel not in seen:
            seen.add(t.source_rel)
            items.append(f"- {md_link(t.source_rel)}")
    return "\n".join(items) or "—"


