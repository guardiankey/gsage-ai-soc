def render_index(groups: list[ToolGroup], paths: dict[str, Path], output_dir: Path) -> str:
    parts: list[str] = []
    parts.append(AUTOGEN_BANNER)
    parts.append("# MCP Tools — Operator Reference\n")
    parts.append(textwrap.dedent("""\
        This index lists every MCP tool available in the running server,
        grouped by source category. Each linked page is auto-generated from
        the tool's Python source — see the **Source files** section at the
        bottom of every page to find the canonical definitions.

        **How configuration works:**

        1. Tools are configured at the *organisation* level (and optionally
           at the *department* level) via the **configtool model** in the
           web UI. The JSON shape of each model is documented on the tool's
           page under **Configuration**.
        2. Default values can also be set via environment variables in the
           backend's `.env`. The exact variable names are derived from each
           tool's schema and listed under **Environment variables** on each
           page; a consolidated dump lives in
           [`.env.tools.example`](../.env.tools.example) at the repo root.
        3. **Sensitive values** (API keys, passwords, tokens) should be set
           via env vars or directly in the encrypted DB row — *never*
           inlined in the JSON.
        4. Tools marked **multi-config** support several profiles per org
           with a distinct `profile_id` each.

        See [`docs/dev/TOOLS.md`](../dev/TOOLS.md) for the underlying
        framework (audience: developers).
    """))

    # Bucket by category.
    by_category: dict[str, list[ToolGroup]] = {}
    for g in groups:
        cat = g.tools[0].category if g.tools else "general"
        by_category.setdefault(cat, []).append(g)

    for category in sorted(by_category.keys()):
        parts.append(f"## {category}\n")
        rows = [
            "| Group / Tool | Tools | Permissions | Approval | Config | Multi-cfg | Page |",
            "| --- | ---: | --- | :---: | :---: | :---: | --- |",
        ]
        cat_groups = sorted(by_category[category], key=lambda g: g.group_id)
        for g in cat_groups:
            page = paths.get(g.group_id)
            if page is None:
                continue
            try:
                rel_link = str(page.relative_to(output_dir))
            except ValueError:
                rel_link = page.name
            tool_count = len(g.tools)
            perms = sorted({p for t in g.tools for p in t.permissions})
            perms_str = ", ".join(f"`{p}`" for p in perms) or "—"
            requires_appr = any(t.requires_approval for t in g.tools)
            has_config = any(t.has_config for t in g.tools)
            multi = any(t.supports_multiple_configs for t in g.tools)
            rows.append(
                "| `{gid}` | {n} | {perms} | {appr} | {cfg} | {mc} | [{rel}]({rel}) |".format(
                    gid=g.group_id,
                    n=tool_count,
                    perms=md_escape_cell(perms_str),
                    appr=md_bool(requires_appr),
                    cfg=md_bool(has_config),
                    mc=md_bool(multi),
                    rel=rel_link,
                )
            )
        parts.append("\n".join(rows) + "\n")

    return "\n".join(parts).rstrip() + "\n"


