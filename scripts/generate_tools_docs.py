def group_tools(tools: list[ToolInfo]) -> list[ToolGroup]:
    """Group tools by ``config_namespace``; singletons keep their own group."""
    groups: dict[str, ToolGroup] = {}
    for t in tools:
        if t.config_namespace:
            key = t.config_namespace
            grp = groups.setdefault(
                key, ToolGroup(group_id=key, namespace=key)
            )
        else:
            key = f"__single__::{t.name}"
            grp = groups.setdefault(
                key, ToolGroup(group_id=t.name, namespace=None)
            )
        grp.tools.append(t)

    # Stable internal ordering.
    for grp in groups.values():
        grp.tools.sort(key=lambda x: x.name)

    return sorted(groups.values(), key=lambda g: (g.namespace or g.group_id))


def output_path_for(group: ToolGroup, output_dir: Path) -> Path:
    """Compute the Markdown output path for a group.

    Rules:
      - If ≥2 tools share the same source directory under
        ``src/mcp_server/tools/`` → ``<parent>/<dir_name>.md``.
      - Otherwise (singleton) → ``<dir>/<tool_filename_stem>.md``.
    """
    sources = [Path(t.source_rel) for t in group.tools if t.source_rel]
    if not sources:
        return output_dir / f"{group.group_id}.md"

    # Strip the prefix `src/mcp_server/tools/` to get a tools-relative path.
    rel_paths: list[Path] = []
    prefix = Path("src/mcp_server/tools")
    for s in sources:
        try:
            rel_paths.append(s.relative_to(prefix))
        except ValueError:
            rel_paths.append(s)

    # All tool dirs (parent of each .py).
    dirs = {p.parent for p in rel_paths}

    if len(rel_paths) >= 2 and len(dirs) == 1:
        # Family living together in one folder. Promote one level up only
        # when the folder name matches the namespace (or group id) — this
        # avoids generating ambiguous files like ``core.md`` for a family
        # that simply happens to share the ``core/`` directory.
        only_dir = next(iter(dirs))
        if str(only_dir) in (".", ""):
            return output_dir / f"{group.group_id}.md"
        if only_dir.name in {group.namespace, group.group_id}:
            return output_dir / only_dir.parent / f"{only_dir.name}.md"
        # Folder name doesn't match the family — keep the file inside the
        # folder, named after the namespace / group id.
        return output_dir / only_dir / f"{group.group_id}.md"

    # Singleton (or split family): doc next to the first source file.
    stem = rel_paths[0].stem if not group.is_family else group.group_id
    return output_dir / rel_paths[0].parent / f"{stem}.md"


