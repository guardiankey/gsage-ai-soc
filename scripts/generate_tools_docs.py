# Main entry point
# ──────────────────────────────────────────────────────────────────────


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate operator-facing Markdown docs for every MCP tool.",
    )
    parser.add_argument(
        "--actions-depth",
        choices=("brief", "medium"),
        default="brief",
        help="How much detail to render for action-driven tools (default: brief).",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=DEFAULT_OUTPUT_DIR,
        help=f"Where to write the .md files (default: {DEFAULT_OUTPUT_DIR.relative_to(ROOT)}).",
    )
    parser.add_argument(
        "--env-file",
        type=Path,
        default=DEFAULT_ENV_FILE,
        help=f"Where to write the consolidated env file (default: {DEFAULT_ENV_FILE.relative_to(ROOT)}).",
    )
    parser.add_argument(
        "--only",
        type=str,
        default=None,
        help="Restrict generation to tools whose name matches this regex (debug).",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Dry-run: exit 1 if any output would change. Useful for CI guards.",
    )
    args = parser.parse_args()

    print("Discovering tools …")
    classes = discover_tools()
    if not classes:
        print("ERROR: No tools discovered.", file=sys.stderr)
        return 2

    only_re: Optional[re.Pattern[str]] = re.compile(args.only) if args.only else None

    tool_infos: list[ToolInfo] = []
    for tool_cls in classes.values():
        try:
            info = ToolInfo.from_class(tool_cls)
        except Exception as exc:
            print(f"  [skip] {tool_cls.__name__}: {exc}")
            continue
        if not info.available:
            continue
        # Restrict to the built-in package (custom tools are out of scope for v1).
        if not info.source_rel.startswith("src/mcp_server/tools/"):
            continue
        if only_re and not only_re.search(info.name):
            continue
        tool_infos.append(info)

    tool_infos.sort(key=lambda t: t.name)
    groups = group_tools(tool_infos)
    print(f"  → {len(tool_infos)} tools across {len(groups)} groups.")

    # Compute output paths first (used by index too).
    paths: dict[str, Path] = {}
    for group in groups:
        paths[group.group_id] = output_path_for(group, args.output_dir)

    changed: list[Path] = []

    # Per-group docs.
    for group in groups:
        target = paths[group.group_id]
        content = render_group_doc(group, args.actions_depth)
        if write_if_changed(target, content, check_only=args.check):
            changed.append(target)

    # Index page.
    index_path = args.output_dir / "README.md"
    index_content = render_index(groups, paths, args.output_dir)
    if write_if_changed(index_path, index_content, check_only=args.check):
        changed.append(index_path)

    # .env.tools.example
    env_content = render_env_file(groups)
    if write_if_changed(args.env_file, env_content, check_only=args.check):
        changed.append(args.env_file)

    if args.check:
        if changed:
            print("CHECK FAILED — the following files would change:")
            for p in changed:
                try:
                    rel = p.relative_to(ROOT)
                except ValueError:
                    rel = p
                print(f"  - {rel}")
            return 1
        print("OK — generated docs are in sync with the codebase.")
        return 0

    if changed:
        print(f"Wrote / updated {len(changed)} file(s):")
        for p in changed:
            try:
                rel = p.relative_to(ROOT)
            except ValueError:
                rel = p
            print(f"  - {rel}")
    else:
        print("No changes — docs already in sync.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
