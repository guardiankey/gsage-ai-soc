def render_env_file(groups: list[ToolGroup]) -> str:
    out: list[str] = [ENV_FILE_BANNER, ""]
    for group in groups:
        if not any(t.has_config for t in group.tools):
            continue

        title = group.display_title
        out.append(f"# === {title} ===")
        out.append("")

        # Shared (namespace) block when applicable.
        namespace_fields: set[str] = set()
        if group.namespace and group.is_family:
            ns_props, _, ns_defaults = merge_schemas(group.tools)
            namespace_fields = set(ns_props.keys())
            for fname in sorted(ns_props.keys()):
                finfo = ns_props[fname]
                var = env_var_name(group.namespace, fname)
                desc = finfo.get("description")
                if desc:
                    out.append(f"# {desc}")
                if finfo.get("sensitive"):
                    out.append("# sensitive: store securely (do NOT commit real values)")
                    out.append(f"{var}=__SET_ME__")
                else:
                    default = field_default(ns_props, ns_defaults, fname)
                    out.append(f"{var}={_env_value_repr(default)}")
                out.append("")

        # Per-tool overrides — only fields NOT covered by the shared block.
        for t in group.tools:
            if t.config_namespace and t.config_namespace == t.name:
                continue
            props = schema_properties(t.config_schema)
            if not props and not t.config_defaults:
                continue
            scope = t.name
            block_added = False
            for fname in sorted(props.keys() | t.config_defaults.keys()):
                if fname in namespace_fields:
                    continue
                finfo = props.get(fname, {})
                var = env_var_name(scope, fname)
                if not block_added:
                    out.append(f"# --- override: {t.name} ---")
                    block_added = True
                desc = finfo.get("description")
                if desc:
                    out.append(f"# {desc}")
                if finfo.get("sensitive"):
                    out.append("# sensitive: store securely (do NOT commit real values)")
                    out.append(f"{var}=__SET_ME__")
                else:
                    default = field_default(props, t.config_defaults, fname)
                    out.append(f"{var}={_env_value_repr(default)}")
                out.append("")

        out.append("")
    return "\n".join(out).rstrip() + "\n"


def _env_value_repr(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)):
        return str(value)
    if isinstance(value, (list, dict)):
        return json.dumps(value, ensure_ascii=False)
    return str(value)


# ──────────────────────────────────────────────────────────────────────
# IO helpers (idempotent writes + --check)
# ──────────────────────────────────────────────────────────────────────


def write_if_changed(path: Path, content: str, *, check_only: bool) -> bool:
    """Return True if content differs from disk (and write unless check_only)."""
    existing = path.read_text(encoding="utf-8") if path.exists() else None
    if existing == content:
        return False
    if not check_only:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
    return True


