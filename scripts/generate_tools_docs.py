#!/usr/bin/env python3
"""Generate operator-facing Markdown documentation for every MCP tool.

This script introspects each registered ``BaseTool`` subclass, groups
tools by ``config_namespace`` (or by tool name for singletons), and
overwrites Markdown files under ``docs/tools/`` plus a derived
``.env.tools.example`` listing every configuration-derived environment
variable.

The source of truth is the tool ``.py`` files (ClassVars, schemas,
docstrings).  The output is auto-generated and **must not be edited by
hand** — the banner at the top of every file states this explicitly.

Usage::

    python scripts/generate_tools_docs.py
    python scripts/generate_tools_docs.py --actions-depth medium
    python scripts/generate_tools_docs.py --check    # exit 1 on diff
    python scripts/generate_tools_docs.py --only "trellix.*"

The script is idempotent — running twice in a row produces zero diff.
"""

